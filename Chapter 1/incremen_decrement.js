let score = 5
score++
console.log(score)
++score
console.log(score)

// Difference between post increment and pre increment 
let a = 5
let b = a++
console.log(b)
let c = ++a
console.log(c)

// In post increment the value is incremented after the value is assigned to the variable
// In pre increment the value is incremented before the value is assigned to the variable